@mech_atlas = 100
@mech_kkrab = 100
# АТАКА
def attack(robots)
sleep 1 # добавим sleep для красоты
i = rand(0..50) #ОБЪЯВЛЯЕМ ПЕРЕМЕННУЮ СЛУЧАЙНОЙ ВЕЛИЧЕНЫ АТАКИ
    if robots == @mech_atlas
        @mech_atlas = robots - i
    elsif robots == @mech_kkrab
        @mech_kkrab = robots - i
    end

puts "Противнику нанесено #{i} повреждений"
sleep 1 # еще один sleep для красоты вывода
end
###############################
# ПРОВЕРКА ПОБЕДЫ
###############################
def victory?
    if @mech_atlas <= 0
        puts "Мех Атлас победил"
    return true
    end
    if @mech_kkrab <= 0
        puts "Мех Камчатский краб победил"
    return true
    end
end
###############################
# СТАТИСТИКА
###############################
