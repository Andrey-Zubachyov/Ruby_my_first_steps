# Метод принимает массив для атаки
def attack(arr)
sleep 1 # добавим sleep для красоты
i = rand(0..9)
    if arr[i] == 1
        arr[i] = 0
        puts "Робот по индексу #{i} уничтожен"
    else
        puts "Промазали по индексу #{i}"
    end
sleep 1 # еще один sleep для красоты вывода
end
